//use the comparison operator
#include <stdio.h>
int main()
{
    int a=5,b=10;
    //grater than operator
    printf("%d\n",a>-1);
    //less than operator
    printf("%d\n",a<-1);
    //greater than or equal
    printf("%d\n" ,b>=1);
    //less than or equal
    printf("%d\n",a<=5);
    //equal to
    printf("%d\n",b==10);
    //not equal to
    printf("%d\n",a!=5);
    return 0;
}